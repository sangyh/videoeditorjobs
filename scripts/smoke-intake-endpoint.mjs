async function getHealth() {
  const response = await fetch(endpoint);
  const text = await response.text();
  let json;
  try {
    json = JSON.parse(text);
  } catch {
    throw new Error(`Health check did not return JSON. HTTP ${response.status}: ${text.slice(0, 300)}`);
  }

  if (!json.ok) {
    throw new Error(`Health check returned ok=false: ${JSON.stringify(json)}`);
  }

  if (json.scriptVersion !== expectedScriptVersion) {
    throw new Error(
      `Health check returned scriptVersion ${JSON.stringify(json.scriptVersion)}; expected ${expectedScriptVersion}. Redeploy the latest Apps Script.`
    );
  }

  if (json.spreadsheetId !== "19Hx9jc-cMI2yjYAzSG5NVca8yABCTLygomKs59CuTXI") {
    throw new Error(`Health check returned unexpected spreadsheetId: ${JSON.stringify(json.spreadsheetId)}`);
  }

  return json;
}

async function post(payload) {
  const response = await fetch(endpoint, {
    method: "POST",
    headers: {
      "Content-Type": "text/plain;charset=utf-8",
    },
    body: JSON.stringify(payload),
  });
  const text = await response.text();
  let json;
  try {
    json = JSON.parse(text);
  } catch {
    throw new Error(`Endpoint did not return JSON. HTTP ${response.status}: ${text.slice(0, 300)}`);
  }

  if (!json.ok) {
    throw new Error(`Endpoint returned ok=false for ${payload.kind}: ${JSON.stringify(json)}`);
  }

  if (json.scriptVersion !== expectedScriptVersion) {
    throw new Error(
      `Endpoint returned scriptVersion ${JSON.stringify(json.scriptVersion)} for ${payload.kind}; expected ${expectedScriptVersion}.`
    );
  }

  if (!json.triage || !json.triage.source_bucket || typeof json.triage.lead_score === "undefined") {
    throw new Error(`Endpoint response missing triage fields for ${payload.kind}: ${JSON.stringify(json)}`);
  }

  if (!json.confirmation || typeof json.confirmation.sent === "undefined") {
    throw new Error(`Endpoint response missing confirmation result for ${payload.kind}: ${JSON.stringify(json)}`);
  }

  return json;
}

